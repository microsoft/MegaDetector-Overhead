import os
from typing import Callable, Dict, Optional

import numpy
import torch
import wandb

from ..utils.logger import CustomLogger
from .evaluators import EVALUATORS, HerdNet_Detection_Branch_Evaluator
from .metrics import Metrics
from .stitchers import Stitcher


def _source_from_image_name(image_name: str) -> str:
    """Pull out the `<source>` part of a `<source>_<basename>` merged filename.

    Falls back to the literal first-underscore split. If the filename has no
    underscore (shouldn't happen in our merged folder), returns the whole name.
    """
    base = os.path.basename(str(image_name))
    if "_" not in base:
        return "unknown"
    return base.split("_", 1)[0]


@EVALUATORS.register()
class HerdNetPerSourceEvaluator(HerdNet_Detection_Branch_Evaluator):
    """Drop-in replacement for HerdNet_Detection_Branch_Evaluator that also
    computes per-source F1 + macro-F1.

    Returns:
      ``f1_macro`` — unweighted mean of per-source F1.

    Logs (via the CustomLogger and wandb if enabled):
      - per-source F1, recall, precision
      - global F1 / recall / precision (same as the parent evaluator)
      - f1_macro (the headline number)
    """

    def __init__(
        self,
        model: torch.nn.Module,
        dataloader: torch.utils.data.DataLoader,
        metrics: Metrics,
        lmds_kwargs: Optional[Dict] = None,
        device_name: str = "cuda",
        print_freq: int = 10,
        stitcher: Optional[Stitcher] = None,
        vizual_fn: Optional[Callable] = None,
        work_dir: Optional[str] = None,
        header: Optional[str] = None,
    ) -> None:
        super().__init__(
            model=model,
            dataloader=dataloader,
            metrics=metrics,
            lmds_kwargs=lmds_kwargs or {"kernel_size": (3, 3)},
            device_name=device_name,
            print_freq=print_freq,
            stitcher=stitcher,
            vizual_fn=vizual_fn,
            work_dir=work_dir,
            header=header,
        )
        # Lazily-populated dict of per-source Metrics clones. Created on first
        # encounter so the source list need not be known up front.
        self._per_source_metrics: Dict[str, Metrics] = {}

    def _get_metrics_for_source(self, source: str) -> Metrics:
        if source not in self._per_source_metrics:
            self._per_source_metrics[source] = self.metrics.copy()
            self._per_source_metrics[source].flush()
        return self._per_source_metrics[source]

    def evaluate(
        self,
        returns: str = "f1_macro",
        wandb_flag: bool = False,
        viz: bool = False,
        log_meters: bool = True,
    ) -> float:
        """Run validation; return the requested metric.

        ``returns`` defaults to ``f1_macro``. Other accepted values are passed
        through to the parent class's notion of `recall`/`precision`/`f1_score`,
        but those refer to the GLOBAL metrics.
        """
        self.model.eval()
        self.metrics.flush()
        self._per_source_metrics = {}

        logger = CustomLogger(
            delimiter=" ", filename=self.logs_filename, work_dir=self.work_dir
        )

        for i, (images, targets) in enumerate(
            logger.log_every(self.dataloader, self.print_freq, self.header)
        ):
            images, targets = self.prepare_data(images, targets)
            if self.stitcher is not None:
                output_heatmap = self.stitcher(images[0])
                output_heatmap = self.post_stitcher(output_heatmap)
            else:
                output_heatmap, _ = self.model(images)
                if isinstance(output_heatmap, tuple):
                    output_heatmap = output_heatmap[0]

            output = self.prepare_feeding(targets, output_heatmap)

            # Identify the source for this batch (CSVDataset uses batch_size=1 in
            # eval; image_name is always a single-element list).
            image_name = targets.get("image_name", ["unknown"])[0]
            source = _source_from_image_name(image_name)

            # Feed both the global metrics and the per-source bucket
            self.metrics.feed(**output)
            self._get_metrics_for_source(source).feed(**output)

        # Aggregate global
        self._stored_metrics = self.metrics.copy()
        self.metrics.aggregate()

        # Aggregate per-source + collect F1s
        per_source_results: Dict[str, Dict[str, float]] = {}
        for source, m in self._per_source_metrics.items():
            m.aggregate()
            per_source_results[source] = {
                "f1": float(m.fbeta_score()),
                "recall": float(m.recall()),
                "precision": float(m.precision()),
                "n_targets": int(sum(m.tp) + sum(m.fn)),
            }

        # Macro-F1 = unweighted mean over sources that actually had targets.
        active_sources = [
            s for s, r in per_source_results.items() if r["n_targets"] > 0
        ]
        if active_sources:
            f1_macro = float(
                numpy.mean([per_source_results[s]["f1"] for s in active_sources])
            )
        else:
            f1_macro = 0.0

        # Log per-source + macro
        if logger.logger is not None:
            logger.logger.info(f"{self.header} PER-SOURCE F1:")
            for source in sorted(per_source_results.keys()):
                r = per_source_results[source]
                logger.logger.info(
                    f"  {source:>20s}  F1={r['f1']:.4f}  R={r['recall']:.4f}  "
                    f"P={r['precision']:.4f}  n={r['n_targets']}"
                )
            logger.logger.info(
                f"{self.header} f1_macro={f1_macro:.4f}  "
                f"f1_global={self.metrics.fbeta_score():.4f}  "
                f"sources={sorted(per_source_results.keys())}"
            )

        if wandb_flag:
            wandb.run.summary["f1_macro"] = f1_macro
            wandb.run.summary["f1_global"] = float(self.metrics.fbeta_score())
            wandb.run.summary["recall_global"] = float(self.metrics.recall())
            wandb.run.summary["precision_global"] = float(self.metrics.precision())
            for source, r in per_source_results.items():
                wandb.run.summary[f"f1__{source}"] = r["f1"]
                wandb.run.summary[f"recall__{source}"] = r["recall"]
                wandb.run.summary[f"precision__{source}"] = r["precision"]

        # Stash for downstream callers / debugging
        self._per_source_results = per_source_results
        self._f1_macro = f1_macro

        if returns == "f1_macro":
            return f1_macro
        if returns == "recall":
            return float(self.metrics.recall())
        if returns == "precision":
            return float(self.metrics.precision())
        if returns == "f1_score":
            return float(self.metrics.fbeta_score())
        # Defer anything else to the global stored metrics
        return float(self.metrics.fbeta_score())
