"""Per-source evaluator subclass for HerdNet detection-branch models.

Buckets validation predictions by a `<source>_` filename prefix (set during
the all-7-datasets merge, see
`Aerial_datasets/scripts/preprocessing/merge_all_training_patches.py`),
computes F1 per source, and reports macro-F1 across sources.

This avoids letting any single dataset (e.g. Caribou with ~7k of ~15k
validation patches) dominate the headline score.

Designed to be a near drop-in replacement for `HerdNet_Detection_Branch_Evaluator`:
the YAML config switches `evaluator.name` to `HerdNetPerSourceEvaluator` and
`validate_on` to `f1_macro`.
"""

